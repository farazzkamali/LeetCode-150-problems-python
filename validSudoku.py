class Solution:
    def isValidSudoku(board):
        # Initialize sets to keep track of seen digits in rows, columns, and sub-boxes
        rows = [set() for _ in range(9)]
        columns = [set() for _ in range(9)]
        boxes = [set() for _ in range(9)]

        for i in range(9):
            for j in range(9):
                num = board[i][j]
                if num != ".":
                    # Check row
                    if num in rows[i]:
                        return False
                    rows[i].add(num)

                    # Check column
                    if num in columns[j]:
                        return False
                    columns[j].add(num)

                    # Check sub-box
                    box_index = (i // 3) * 3 + j // 3
                    if num in boxes[box_index]:
                        return False
                    boxes[box_index].add(num)

        return True


